import enum


@enum.unique
class ErrorCode(enum.IntEnum):
    # General error codes (1001-1999)
    GENERAL_1001_UNEXPECTED_ERROR = 1001
    GENERAL_1002_REQUEST_VALIDATION_FAILED = 1002
    GENERAL_1003_RESOURCE_NOT_FOUND = 1003
    GENERAL_1004_HTTP_SERVICE_ERROR = 1004
    GENERAL_1005_DUPLICATE_RECORD = 1005
    GENERAL_1006_UNAUTHORIZED_BEHAVIOR = 1006
    GENERAL_1007_NETWORK_ERROR = 1007
    GENERAL_1011_NOT_IMPLEMENTED = 1011

    # Line API service error (2001-2999)
    LINE_SERVICE_ERROR = 2001
    LINE_TIMEOUT_ERROR = 2002
